package Calculadora;

public class Calculadora {

	public static int suma(int a, int b) {
		int resultado = a + b;
		return resultado;
	}

	public static int resta(int a, int b) {
		int resultado = a - b;
		return resultado;
	}

	public static int multiplicar(int a, int b) {
		int resultado = a * b;
		return resultado;
	}
	
	public static int dividir(int a, int b) {
		int resultado = a / b;
		return resultado;
	}
}
